  test("group common sequences") {
    val groups = groupCommonSequences(List("A", "C", "T", "G"))
    expect(2) { groups.size }
    expect(List("A", "T")) { groups(Set("A", "T")).toList }
    expect(List("C", "G")) { groups(Set("G", "C")).toList }
  }
  
  test("get errors") {
    val groups = List("AT", "AT", "GA", "CT", "TC")
    // reverse complements: AT->AT, GA->TC, CT->AG
    expect(List("CT")) { partitionErrors(groups)._1.toList }
  }
  
